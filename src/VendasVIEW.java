    public void carregarProdutosVendidos() {
        DefaultTableModel model = (DefaultTableModel) tableVendidos.getModel();

        ProdutosDAO produtosDAO = new ProdutosDAO();
        ArrayList<ProdutosDTO> listaProdutos = produtosDAO.listarProdutosVendidos();
        for (ProdutosDTO produto : listaProdutos) {
            model.addRow(new Object[]{
                produto.getId(),
                produto.getNome(),
                produto.getValor(),
                produto.getStatus()
            });
        }
    
    
    
    }

